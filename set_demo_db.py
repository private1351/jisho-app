def _add_words(dictionary_id: int, page_index: int, items: Sequence[tuple[str, str, int]]) -> None:
    for w, d, line_count in items:
        db.session.add(
            Word(
                word=w,
                definition=d,
                line_count=line_count,
                page_index=page_index,
                dictionary_id=dictionary_id,
            )
        )


